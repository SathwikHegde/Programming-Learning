WITH RECURSIVE ReportingChain AS (
    -- Base case: direct reports to head of company (employee_id = 1)
    SELECT employee_id, employee_name, manager_id, 1 as level
    FROM Employees
    WHERE manager_id = 1 AND employee_id != 1

    UNION ALL

    -- Recursive case: find indirect reports
    SELECT e.employee_id, e.employee_name, e.manager_id, rc.level + 1
    FROM Employees e
    INNER JOIN ReportingChain rc ON e.manager_id = rc.employee_id
    WHERE e.employee_id != 1
)

SELECT DISTINCT employee_id
FROM ReportingChain
WHERE level <= 3
ORDER BY employee_id;
